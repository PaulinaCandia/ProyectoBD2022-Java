package clases;

public class Creditos {
	int ID_CREDITO;
	String NOMBRE;
	String DESCRIPCION;
	float TASA_INTERES;
	int CUOTAS_MAX;
	
	public int getID_CREDITO() {
		return ID_CREDITO;
	}
	public void setID_CREDITO(int iD_CREDITO) {
		ID_CREDITO = iD_CREDITO;
	}
	public String getNOMBRE() {
		return NOMBRE;
	}
	public void setNOMBRE(String nOMBRE) {
		NOMBRE = nOMBRE;
	}
	public String getDESCRIPCION() {
		return DESCRIPCION;
	}
	public void setDESCRIPCION(String dESCRIPCION) {
		DESCRIPCION = dESCRIPCION;
	}
	public float getTASA_INTERES() {
		return TASA_INTERES;
	}
	public void setTASA_INTERES(float tASA_INTERES) {
		TASA_INTERES = tASA_INTERES;
	}
	public int getCUOTAS_MAX() {
		return CUOTAS_MAX;
	}
	public void setCUOTAS_MAX(int cUOTAS_MAX) {
		CUOTAS_MAX = cUOTAS_MAX;
	}

	
	

}
